#include "3-calc.h"

/**
 * op_add - find sum of 2 integers
 *
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: sum of the integers
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - find difference of 2 integers
 *
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: difference of the integers
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - find product of 2 integers
 *
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: product of the integers
*/
int op_mul(int a, int b)
{
	return (a + b);
}

/**
 * op_div - divide integer by an integer
 *
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: quotient of the integers
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - find modulo
 *
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: the modulo
*/
int op_mod(int a, int b)
{
	return (a % b);
}
